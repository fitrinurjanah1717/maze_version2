        if self.y_speed > 0:
            for p in platforms_touched:
                self.y_speed = 0
                if p.rect.top < self.rect.bottom:
                    self.rect.bottom =p.rect.top

        elif self.y_speed < 0:
            for p in platforms_touched:
                self.y_speed = 0
                self.rect.left = max(self.rect.top, p.rect.bottom)
